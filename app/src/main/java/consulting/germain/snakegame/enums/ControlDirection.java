package consulting.germain.snakegame.enums;

/**
 * Created by mark_local on 11/09/2015.
 * enum of the possible control directions expressed as compass points
 */
public enum ControlDirection {
    /** y decreasing */
      NORTH
    /** x increasing */
    , EAST
    /** y increasing */
    , SOUTH
    /** x decreasing */
    , WEST
    /** x increasing y decreasing */
    , NORTH_EAST
    /** x increasing y increasing */
    , SOUTH_EAST
    /** x decreasing y increasing */
    , SOUTH_WEST
    /** x decreasing y decreasing */
    , NORTH_WEST
}
